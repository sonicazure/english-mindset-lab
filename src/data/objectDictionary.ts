export function lookupObject(cn: string): string[] | null {
  if (objectDictionary[cn]) {
    return objectDictionary[cn];
  }
  const cleaned = cn.replace(/的/g, '').replace(/子$/, '');
  if (objectDictionary[cleaned]) {
    return objectDictionary[cleaned];
  }
  for (const [key, values] of Object.entries(objectDictionary)) {
    if (key.includes(cn) || cn.includes(key)) {
      return values;
    }
  }
  return null;
}
